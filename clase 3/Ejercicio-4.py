flotante_1 = float(input("Ingrese numero 1 a evaluar: "))
flotante_2 = float(input("Ingrese numero 2 a evaluar: "))

print("Operaciones")
print(f"{flotante_1} + {flotante_2} = {flotante_1 + flotante_2}")
print(f"{flotante_1} - {flotante_2} = {flotante_1 - flotante_2}")
print(f"{flotante_1} * {flotante_2} = {flotante_1 * flotante_2}")
print(f"{flotante_1} / {flotante_2} = {flotante_1 / flotante_2}")
print(f"{flotante_1} // {flotante_2} = {flotante_1 // flotante_2}")
print(f"{flotante_1} % {flotante_2} = {flotante_1 % flotante_2}")
